// 75. JavaScript While Loops:

var i = 0;
while (i < 20) {
    document.write(i);
    document.write("<br>");
    i++;   
}
/*
Here we've instructed our script to output the value of i as long as the value doesn't exceed 20 the while loop will loop over and over 19 times in this case each time it will output the value of i started at 0 and incremented by 1.
*/