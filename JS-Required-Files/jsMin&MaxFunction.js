// 61. JavaScript Min & Max Function:

// Min Function:

document.getElementById('minFunction').innerHTML = "Minimum number is: " + 
Math.min(600, 300, 50, 75, -5, -150);

// Max Function:

document.getElementById('maxFunction').innerHTML = "Maximum number is: " + 
Math.max(600, 300, 50, 75, -5, -150);