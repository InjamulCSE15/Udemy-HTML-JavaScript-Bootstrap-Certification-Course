// JavaScript Data Types:
// 01. Numbers:

var age = 24;
console.log(typeof(age)); // Output: number

// 02. Strings:

var lastName = "Ismam";
console.log(typeof(lastName)); // Output: string

// 03. Arrays:

var cars = ["BMW", "Volvo", "Ford"];
console.log(typeof(cars));

// 04. Object:
 var person = {
     firstName: "Injamul",
     lastName: "Alam",
     age: 50
 }

 console.log(typeof(person)); // Output: object